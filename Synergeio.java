    public void setDate(JTextField txtField, String dateString)
    {
        Date date = null;
        try{
            if ((dateString != null) && (dateString.length() > 0))
                date = dateFormat.parse(dateString);
        } catch (ParseException e)	{
            date = null;
        }
        this.setDate(txtField, date);
    }
    /**
     * Validate and set the datetime field on the screen given a date.
     * @param txtField
     * @param date The date object
     */
    public void setDate(JTextField txtField, Date date)
    {
        String dateString = "";
        SimpleDateFormat ft = new SimpleDateFormat ("yyyy-MM-dd");
        if (date != null)
            dateString = ft.format(date);
        
        txtField.setText(dateString);
    }

